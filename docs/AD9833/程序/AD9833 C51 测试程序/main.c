sbit LED = P5^5;



/*
*********************************************************************************************************
*	函 数 名: DelayMs
*	功能说明: 时钟延时
*	形    参: ms 以毫秒为单位.
*	返 回 值: 无
*********************************************************************************************************
*/
void DelayMs( unsigned int ms)
{
	unsigned int i,j;
	for (i = 0; i < ms; i++)
		for(j=0;j<1074;j++);
}



